	<h2>2. ¿QUÉ SON LAS COOKIES?</h2>
	<p>Una cookie es un pequeño fichero de texto que los sitios web instalan en el ordenador o el dispositivo
		móvil de
		los usuarios que los visitan. Las cookies realizan muchas tareas diferentes, y por ejemplo nos permiten
		saber el
		número de veces que el Sitio Web ha sido visitado anteriormente por el usuario, identificar si el mismo
		se ha
		registrado previamente, recordar sus acciones previas y preferencias para que no tenga que volver a
		configurarlas cuando regrese al Sitio Web (identificador de inicio de sesión, contraseña, idioma, tamaño
		de
		letra y otras preferencias de visualización), así como permitir una navegación entre las páginas de
		forma segura
		y estable.</p>
	<p>Las cookies empleadas en el Sitio Web por sí mismas no revelan información personal. Su finalidad
		analítica
		emplea identificadores, incluida la IP, con la exclusiva finalidad de identificar el número de
		visitantes únicos
		a la web, su origen geográfico o su tendencia de uso web, pero no identifican personalmente al Usuario.
	</p>

	<h2>3. TIPOLOGÍA Y FINALIDAD DE COOKIES</h2>
	<p>A continuación, ofrecemos información sobre el tipo y finalidad de cookies utilizadas en el Sitio Web:
	</p>
	<ul>
		<li>Cookies “estrictamente necesarias”: Son aquellas cookies consideradas imprescindibles para la
			navegación por
			el Sitio Web, pues le facilitan la utilización de sus prestaciones o herramientas.</li>
		<li>Cookies de funcionalidad y analíticas: Son aquellas cookies, propias o de terceros, que nos permiten
			proporcionarle una buena navegación durante la visita a nuestro Sitio Web. Nos permiten analizar el
			uso del
			Sitio Web para que podamos realizar métricas y mejorar el rendimiento del mismo.</li>
		<li>Cookies de publicidad comportamental: Son aquellas que almacenan información del comportamiento de
			los
			usuarios obtenida a través de la observación continuada de sus hábitos de navegación, lo que permite
			desarrollar un perfil específico para mostrar publicidad en función del mismo.</li>
	</ul>

