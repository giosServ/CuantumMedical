	<h1>POLÍTICA DE PRIVACIDAD DEL SITIO WEB</h1>
	<h2>1. INTRODUCCIÓN</h2>
	<p>La Política de Privacidad del Sitio Web, tiene por objeto facilitar información sobre los derechos y obligaciones
		específicos que asisten a los usuarios (en adelante, los “Usuarios”) de los sitios web, aplicaciones y cualquier
		otra plataforma digital o medio electrónico de CUANTUM MEDICAL COSMETICS S.L. y de las sociedades del grupo cuya
		cabecera es dicha sociedad (en adelante, se referirá indistintamente a CUANTUM MEDICAL COSMETICS S.L. y/o las
		sociedades de su grupo como “CUANTUM MEDICAL”), en virtud del Reglamento (UE) 2016/679 del Parlamento Europeo y
		del Consejo, de 27 de abril de 2016, relativo a la protección de las personas físicas en lo que respecta al
		tratamiento de datos personales y a la libre circulación de estos datos (“RGPD”) y a la Ley Orgánica 3/2018, 5
		de diciembre de 2018, de protección de datos personales y garantía de los derechos digitales (“LOPDGDD”).
	<p>Para visitar cualquiera de los sitios web de CUANTUM MEDICAL, aplicaciones y cualquier otra plataforma digital o
		medio electrónico de CUANTUM MEDICAL (en adelante se referirá a todos ellos como el “Sitio Web”), en general no
		es necesario que nos facilite sus datos de carácter personal. Sin embargo, es posible que determinadas
		interacciones con CUANTUM MEDICAL a través del Sitio Web lo requieran.
	<p>Introducir los datos en alguna o algunas de las hojas o formularios de captación de datos implica aceptar la
		presente Política, dando por entendido que ha sido informado de la política de privacidad y del aviso legal del
		Sitio Web y que se compromete a su entero cumplimiento durante la navegación y participación en el mismo.
	<p>CUANTUM MEDICAL podrá comunicar sus datos a terceros que nos ayuden a proporcionar servicios de tecnologías de la
		información, como proveedores de plataforma, servicios de hospedaje, sitios web, etc. En relación con esto
		último, CUANTUM MEDICAL exigirá que estos terceros cumplan con la legislación vigente, si bien la
		responsabilidad será exigible a éstos.
	<p>La presente Política no es de aplicación a sitios web de terceros, incluyendo aquéllos a los que Usted pueda
		tener acceso a través de un enlace localizado en el Sitio Web de CUANTUM MEDICAL.
	<p>Si tiene alguna duda relativa al tratamiento de sus datos personales, puede contactar con CUANTUM MEDICAL o con
		su Delegado de Protección de Datos a través de la siguiente dirección: <a href="mailto:lopd@cuantum.es">
			lopd@cuantum.es</a>.

	<h2>2. DESCRIPCIÓN TRATAMIENTOS DE DATOS PERSONALES</h2>
	<p>2.1. A través del Sitio Web se tratan los datos facilitados por los Usuarios durante la navegación y uso del
		Sitio Web, así como los recogidos a través de los formularios correspondientes.
	<p>A tal efecto, mediante la cumplimentación de aquellos formularios que puedan estar incluidos en el Sitio Web, los
		Usuarios aceptan la inclusión y tratamiento de los datos que proporcionen en un tratamiento de datos de carácter
		personal del cual es responsable CUANTUM MEDICAL, pudiéndose ejercer los derechos pertinentes según lo expuesto
		en la presente política.<br>

	<p>2.2. Datos de contacto:</p>
	<p>Los datos facilitados por el Usuario serán tratados por CUANTUM MEDICAL con la finalidad de atender solicitudes
		de información, dudas, consultas y/o preguntas formuladas. Si es necesario contactarle lo haremos por email.
		El tratamiento de dichos datos está basado en el consentimiento del Usuario (RGPD: 6.1 a) Consentimiento
		prestado por el Usuario. El Usuario podrá revocar el consentimiento en cualquier momento.
		Los datos proporcionados se conservarán hasta la resolución de su solicitud y durante el período máximo de 12
		meses.</p>
	<p>Sus datos pueden ser cedidos a sociedades del grupo cuya cabecera es CUANTUM MEDICAL para las finalidades para
		las cuales ha dado su consentimiento, así como para mantener una gestión integral y centralizada de la relación
		de los Usuarios con las distintas sociedades de CUANTUM MEDICAL y que los Usuarios se puedan beneficiar del
		acceso a sus datos desde cualquiera de ellas, respetando en todo caso la legislación aplicable sobre protección
		de datos de carácter personal y sin necesidad de que a los Usuarios les sea comunicada cada primera comunicación
		que se efectúe.</p>
	<p>Se le informa especialmente de que CUANTUM MEDICAL no toma decisiones automatizadas sobre sus Usuarios.</p>

	<p>2.3. Asimismo, le informamos que la información de las bases de datos podrá ser usada con la finalidad de:
	<ul>
		<li>Ofrecerle una navegación más personalizada como Usuario de nuestro Sitio Web.</li>
		<li>Elaborar estadísticas sobre los países y servidores que visitan más a menudo el Sitio Web.</li>
		<li>Conocer las horas de mayor afluencia de visitas en el Sitio Web así como realizar ajustes precisos para
			evitar problemas de acceso.</li>
	</ul>
	<p>El tratamiento de dichos datos está basado en intereses legítimos (RGPD: 6.1 f: Satisfacción de intereses
		legítimos) perseguidos por el responsable del tratamiento o por un tercero, siempre que sobre dichos
		intereses no prevalezcan los intereses o los derechos y libertades fundamentales del interesado que
		requieran la protección de datos personales, en particular cuando el interesado sea un niño.
	<p>Se le informa especialmente de que CUANTUM MEDICAL no toma decisiones automatizadas sobre sus Usuarios.

	<h2>3. ENLACES</h2>
	<p>En el marco de las comunicaciones indicadas en los apartados anteriores, se le informa que el Sitio Web de
		CUANTUM MEDICAL puede tener enlaces, aplicaciones o funcionalidades compartidas con terceros, como son redes
		sociales o sistemas de comunicación on-line.
	<p>CUANTUM MEDICAL no se hace responsable de las informaciones que se recojan en dichas aplicaciones,
		funcionalidades o redes sociales titularidad de terceros al no tener ninguna capacidad de gestión ni control
		sobre las mismas, siendo de aplicación los avisos legales y las políticas de privacidad que pudieran constar
		en los sitios web o similares de los terceros, por lo que le recomendamos asegurarse que conoce y está de
		acuerdo con sus condiciones legales y normas de privacidad antes de continuar con su uso o facilitar
		cualquier tipo de información personal.

	<h2>4. DERECHOS DE LOS USUARIOS</h2>

	<p>En cualquier momento, Usted podrá ejercitar los derechos reconocidos en el RGPD y en particular los derechos de
		acceso, rectificación, cancelación, oposición, supresión y limitación al tratamiento de sus datos, portabilidad
		y a no ser objeto de decisiones automatizadas, cuando procedan.
		Los derechos referidos en el párrafo precedente podrán ejercitarse a través del envío de una solicitud a
		<a href="mailto:lopd@cuantum.es"> lopd@cuantum.es</a> o a la dirección Edifici Eureka, Campus UAB Bellaterra,
		Barcelona, 08193, aportando copia de su
		DNI u otro documento que acredite su identidad y especificando en su solicitud cuál de estos derechos quiere que
		sea satisfecho.
	<p> caso de que actuara mediante representante, deberá aportar también documento que acredite la representación y
		documento identificativo del mismo.
	<p>Puede consultar los modelos, formularios y más información sobre los referidos derechos en la página oficial de
		la Agencia Española de Protección de Datos (www.aepd.es).
	<p>Asimismo, si Usted ha proporcionado su consentimiento para algún tratamiento o finalidad específicos, podrá
		retirarlo en cualquier momento.
	<p>Usted puede presentar una reclamación ante la autoridad de control (Agencia Española de Protección de Datos) en
		caso de que considere que se ha vulnerado alguno de sus derechos bajo las vigentes disposiciones legales. No
		obstante, de cara a darle el mejor servicio, rogamos se ponga en contacto previamente con CUANTUM MEDICAL por
		los canales de atención indicados anteriormente, con objeto de poder atender más ágilmente su solicitud y
		aclararle las dudas que pudieran surgirle.

	<h2>5. RESPONSABILIDAD DE LOS USUARIOS DEL SITIO WEB POR USO Y CONTENIDO</h2>

	<p>Tanto el acceso al Sitio Web como el uso que pueda realizarse de la información y contenidos incluidos en el
		mismo será de exclusiva responsabilidad de quien lo realice.
	<p>Por tanto, el uso que pueda hacerse de la información, imágenes, contenidos y/o productos reseñados y accesibles
		a través del Sitio Web estará supeditado a la legalidad aplicable, sea nacional o internacional, así como a los
		principios de buena fe y uso lícito por parte de los Usuarios, quienes estarán obligados a hacer un uso
		razonable de los servicios o contenidos bajo el principio de buena fe y con respeto a la legalidad vigente, a la
		moral, al orden público, a las buenas costumbres y a los derechos de terceros o de CUANTUM MEDICAL.
	<p>En este sentido, el Usuario es el responsable de la autenticidad de los datos aportados, comprometiéndose a que
		sean exactos, actuales y completos para la finalidad que sean recogidos, asumiendo la responsabilidad de los
		daños y perjuicios, tanto por lucro cesante como por daño emergente, que se pudieran generar por dichas
		inexactitudes o falsedades.
	<p>Asimismo, si los datos aportados en los correspondientes formularios fueran titular de un tercero, el Usuario se
		hace único responsable de la correcta captación del consentimiento e información al tercero sobre los aspectos
		reflejados en el aviso legal y en la política de privacidad del Sitio Web.</p>

	<h2>6. COOKIES</h2>

	<p>CUANTUM MEDICAL utilizará dispositivos de almacenamiento y recuperación de datos (“Cookies”) cuando el Usuario
		haya dado su consentimiento previo para ello de acuerdo con lo que se indica en la ventana emergente del
		navegador del Usuario cuando accede por primera vez al Sitio Web y en los demás términos y condiciones que se
		indican en la Política de Cookies de CUANTUM MEDICAL que todo Usuario debe conocer.</p>
		

	<h2>7. MENORES</h2>

	<p>Los menores de edad no podrán usar los servicios disponibles a través del Sitio Web sin la previa autorización
		de sus padres, tutores o representantes legales, quienes serán los únicos responsables de todos los actos
		realizados a través del Sitio Web por los menores a su cargo, incluyendo la cumplimentación de los formularios
		con los datos personales de dichos menores y la marcación, en su caso, de las casillas que los acompañan.

	<h2>8. MEDIDAS DE SEGURIDAD</h2>

	<p>CUANTUM MEDICAL adopta los niveles de seguridad requeridos por el RGPD adecuados a la naturaleza de los datos
		que son objeto de tratamiento en cada momento.
	<p>No obstante lo anterior, la seguridad técnica en un medio como internet no es inexpugnable y pueden existir
		actuaciones dolosas de terceros, si bien CUANTUM MEDICAL pone todos los medios a su alcance para evitar dichas
		actuaciones.

	<h2>9. ACTUALIZACIÓN Y REVISIÓN</h2>

	<p>La presente Política será revisada y actualizada siempre que sea necesario para adaptarla a nuestras prácticas,
		servicios y a la normativa que sea de aplicación en cada momento, por lo que le recomendamos revisar esta
		Política cada vez que acceda a nuestro Sitio Web con el objetivo de estar adecuadamente informado sobre cómo y
		para qué tratamos sus datos.
