	<h2>2. QUÈ SÓN LES COOKIES?</h2>
	<p>Una cookie és un petit fitxer de text que els llocs web instaŀlen a l'ordinador o al dispositiu mòbil dels
		usuaris que els visiten. Les cookies realitzen moltes tasques diferents, i per exemple ens permeten saber el
		número de vegades que el Lloc Web ha estat visitat anteriorment per l'usuari, identificar si el mateix s'ha
		registrat prèviament, recordar les seves accions prèvies i preferències perquè no hagi de tornar a
		configurar-les quan torni al Lloc Web (identificador d'inici de sessió, contrasenya, idioma, mida de
		letra i altres preferències de visualització), així com permetre una navegació entre les pàgines de forma segura
		i estable.</p>
	<p>Les cookies emprades en el Lloc Web per si soles no revelen informació personal. La seva finalitat analítica
		empra identificadors, inclosa la IP, amb l'exclusiva finalitat d'identificar el número de visitants únics
		al web, el seu origen geogràfic o la seva tendència d'ús web, però no identifiquen personalment l'Usuari.</p>

	<h2>3. TIPOLOGIA I FINALITAT DE LES COOKIES</h2>
	<p>A continuació, oferim informació sobre el tipus i finalitat de les cookies utilitzades en el Lloc Web:</p>
	<ul>
		<li>Cookies "estrictament necessàries": Són aquelles cookies considerades imprescindibles per a la navegació per
			el Lloc Web, ja que faciliten la utilització de les seves prestacions o eines.</li>
		<li>Cookies de funcionalitat i analítiques: Són aquelles cookies, pròpies o de tercers, que ens permeten
			proporcionar-vos una bona navegació durant la visita al nostre Lloc Web. Ens permeten analitzar l'ús del
			Lloc Web perquè puguem realitzar mètriques i millorar el rendiment del mateix.</li>
		<li>Cookies de publicitat comportamental: Són aquelles que emmagatzemen informació del comportament dels
			usuaris obtinguda a través de l'observació continuada dels seus hàbits de navegació, el que permet
			desenvolupar un perfil específic per mostrar publicitat en funció del mateix.</li>
	</ul>

