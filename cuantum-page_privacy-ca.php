	<h1>POLÍTICA DE PRIVACITAT DEL LLOC WEB</h1>
	<h2>1. INTRODUCCIÓ</h2>
	<p>La Política de Privacitat del Lloc Web té com a objectiu facilitar informació sobre els drets i obligacions
		específics que assisteixen als usuaris (d'ara endavant, els "Usuaris") dels llocs web, aplicacions i qualsevol
		altre plataforma digital o mitjà electrònic de CUANTUM MEDICAL COSMETICS S.L. i de les societats del grup de les
		quals aquesta societat és la capçalera (d'ara endavant, es farà referència indistintament a CUANTUM MEDICAL
		COSMETICS S.L. i/o les
		societats del seu grup com a "CUANTUM MEDICAL"), en virtut del Reglament (UE) 2016/679 del Parlament Europeu i
		del Consell, de 27 d'abril de 2016, relatiu a la protecció de les persones físiques pel que fa al
		tractament de dades personals i a la lliure circulació d'aquestes dades ("RGPD") i a la Llei Orgànica 3/2018, 5
		de desembre de 2018, de protecció de dades personals i garantia dels drets digitals ("LOPDGDD").
	<p>Per visitar qualsevol dels llocs web de CUANTUM MEDICAL, aplicacions i qualsevol altra plataforma digital o
		mitjà electrònic de CUANTUM MEDICAL (d'ara endavant es farà referència a tots ells com a "Lloc Web"), en general
		no
		és necessari que ens faciliti les seves dades de caràcter personal. No obstant això, és possible que
		determinades
		interaccions amb CUANTUM MEDICAL a través del Lloc Web ho requereixin.
	<p>Introduir les dades en alguna o algunes de les fulles o formularis de captació de dades implica acceptar la
		present Política, donant per entès que ha estat informat de la política de privacitat i de l'avís legal del
		Lloc Web i que es compromet al seu ple compliment durant la navegació i participació en el mateix.
	<p>CUANTUM MEDICAL podrà comunicar les seves dades a tercers que ens ajudin a proporcionar serveis de tecnologies de
		la
		informació, com a proveïdors de plataforma, serveis d'allotjament, llocs web, etc. En relació amb això,
		CUANTUM MEDICAL exigirà que aquests tercers compleixin amb la legislació vigent, tot i que la
		responsabilitat serà exigible a aquests tercers.
	<p>La present Política no és d'aplicació a llocs web de tercers, incloent aquells als quals Vostè pugui
		tenir accés a través d'un enllaç localitzat en el Lloc Web de CUANTUM MEDICAL.
	<p>Si té algun dubte relatiu al tractament de les seves dades personals, pot contactar amb CUANTUM MEDICAL o amb
		el seu Delegat de Protecció de Dades a través de la següent adreça: <a href="mailto:lopd@cuantum.es">
			lopd@cuantum.es</a>.

	<h2>2. DESCRIPCIÓ TRACTAMENTS DE DADES PERSONALS</h2>
	<p>2.1. A través del Lloc Web es tracten les dades facilitades pels Usuaris durant la navegació i ús del
		Lloc Web, així com les recollides a través dels formularis corresponents.
	<p>A aquest efecte, mitjançant la complimentació d'aquells formularis que puguin estar inclosos en el Lloc Web, els
		Usuaris accepten la inclusió i tractament de les dades que proporcionin en un tractament de dades de caràcter
		personal del qual és responsable CUANTUM MEDICAL, podent exercir els drets pertinents segons l'exposat
		en la present política.<br>

	<p>2.2. Dades de contacte:</p>
	<p>Les dades facilitades per l'Usuari seran tractades per CUANTUM MEDICAL amb la finalitat d'atendre sol·licituds
		d'informació, dubtes, consultes i/o preguntes formulades. Si és necessari contactar-lo ho farem per correu
		electrònic.
		El tractament d'aquestes dades es basa en el consentiment de l'Usuari (RGPD: 6.1 a) Consentiment
		prestat per l'Usuari. L'Usuari podrà revocar el consentiment en qualsevol moment.
		Les dades proporcionades es conservaran fins a la resolució de la seva sol·licitud i durant el període màxim de
		12
		mesos.</p>
	<p>Les seves dades poden ser cedides a societats del grup de les quals CUANTUM MEDICAL és la capçalera per a les
		finalitats per
		a les quals ha donat el seu consentiment, així com per mantenir una gestió integral i centralitzada de la
		relació
		dels Usuaris amb les diferents societats de CUANTUM MEDICAL i que els Usuaris puguin beneficiar-se de
		l'accés a les seves dades des de qualsevol d'elles, respectant en tot cas la legislació aplicable sobre
		protecció
		de dades de caràcter personal i sense necessitat que als Usuaris els sigui comunicada cada primera comunicació
		que es faci.</p>
	<p>Se li informa especialment que CUANTUM MEDICAL no pren decisions automatitzades sobre els seus Usuaris.</p>

	<p>2.3. Així mateix, l'informem que la informació de les bases de dades podrà ser utilitzada amb la finalitat de:
	<ul>
		<li>Oferir-li una navegació més personalitzada com a Usuari del nostre Lloc Web.</li>
		<li>Elaborar estadístiques sobre els països i servidors que visiten més sovint el Lloc Web.</li>
		<li>Coneixer les hores de major afluència de visites al Lloc Web així com realitzar ajustos precisos per
			evitar problemes d'accés.</li>
	</ul>
	<p>El tractament d'aquestes dades es basa en interessos legítims (RGPD: 6.1 f: Satisfacció d'interessos
		legítims) perseguits pel responsable del tractament o per un tercer, sempre que sobre aquests
		interessos no prevalguin els interessos o els drets i llibertats fonamentals de l'interessat que
		requereixin la protecció de dades personals, en particular quan l'interessat sigui un menor.
	<p>Se li informa especialment que CUANTUM MEDICAL no pren decisions automatitzades sobre els seus Usuaris.

	<h2>3. ENLLAÇOS</h2>
	<p>En el marc de les comunicacions indicades en els apartats anteriors, se li informa que el Lloc Web de
		CUANTUM MEDICAL pot tenir enllaços, aplicacions o funcionalitats compartides amb tercers, com ara xarxes
		socials o sistemes de comunicació en línia.
	<p>CUANTUM MEDICAL no es fa responsable de les informacions que es recullin en aquestes aplicacions,
		funcionalitats o xarxes socials titularitat de tercers ja que no té cap capacitat de gestió ni control
		sobre les mateixes, sent d'aplicació els avisos legals i les polítiques de privacitat que poguessin constar
		en els llocs web o similars dels tercers, per la qual cosa li recomanem assegurar-se que coneix i està d'acord
		amb les seves condicions legals i normes de privacitat abans de continuar amb el seu ús o facilitar
		qualsevol tipus d'informació personal.

	<h2>4. DRETS DELS USUARIS</h2>

	<p>En qualsevol moment, Vostè podrà exercir els drets reconeguts en el RGPD i en particular els drets d'accés,
		rectificació, cancel·lació, oposició, supressió i limitació al tractament de les seves dades, portabilitat
		i a no ser objecte de decisions automatitzades, quan procedeixin.
		Els drets referits en el paràgraf precedent podran exercir-se mitjançant l'enviament d'una sol·licitud a
		<a href="mailto:lopd@cuantum.es"> lopd@cuantum.es</a> o a l'adreça Edifici Eureka, Campus UAB Bellaterra,
		Barcelona, 08193, aportant còpia del seu
		DNI o un altre document que acrediti la seva identitat i especificant en la seva sol·licitud quin d'aquests
		drets vol que
		sigui satisfet.
	<p>En cas que actuïs mitjançant representant, haurà d'aportar també document que acrediti la representació i
		document identificatiu del mateix.
	<p>Pot consultar els models, formularis i més informació sobre els referits drets a la pàgina oficial de
		l'Agència Espanyola de Protecció de Dades (www.aepd.es).
	<p>Així mateix, si Vostè ha proporcionat el seu consentiment per a algun tractament o finalitat específics, podrà
		retirar-lo en qualsevol moment.
	<p>Vostè pot presentar una reclamació davant l'autoritat de control (Agència Espanyola de Protecció de Dades) en
		cas que consideri que s'ha vulnerat algun dels seus drets sota les vigents disposicions legals. No
		obstant això, de cara a donar-li el millor servei, preguem es posi en contacte prèviament amb CUANTUM
		MEDICAL pels
		canals d'atenció indicats anteriorment, amb l'objecte de poder atendre més àgilment la seva sol·licitud i
		aclarir-li els dubtes que poguessin sorgir-li.

	<h2>5. RESPONSABILITAT DELS USUARIS DEL LLOC WEB PER ÚS I CONTINGUT</h2>

	<p>Tant l'accés al Lloc Web com l'ús que es pugui fer de la informació i continguts inclosos en el
		mateix serà d'exclusiva responsabilitat de qui ho faci.
	<p>Per tant, l'ús que es pugui fer de la informació, imatges, continguts i/o productes reseñats i accessibles
		a través del Lloc Web estarà supeditat a la legalitat aplicable, sigui nacional o internacional, així com als
		principis de bona fe i ús lícit per part dels Usuaris, els quals estaran obligats a fer un ús
		raonable dels serveis o continguts sota el principi de bona fe i amb respecte a la legalitat vigent, a la
		moral, a l'ordre públic, a les bones costums i als drets de tercers o de CUANTUM MEDICAL.
	<p>En aquest sentit, l'Usuari és el responsable de l'autenticitat de les dades aportades, comprometent-se a
		que siguin exactes, actuals i completes per a la finalitat que siguin recollides, assumint la responsabilitat
		dels
		danys i perjudicis, tant per lucre cessant com per dany emergent, que es poguessin generar per aquestes
		inexactituds o falsedats.
	<p>Així mateix, si les dades aportades en els corresponents formularis fossin titular d'un tercer, l'Usuari es
		fa únic responsable de la correcta captació del consentiment i informació al tercer sobre els aspectes
		reflexats en l'avís legal i en la política de privacitat del Lloc Web.</p>

	<h2>6. COOKIES</h2>

	<p>CUANTUM MEDICAL utilitzarà dispositius d'emmagatzematge i recuperació de dades ("cookies") quan l'Usuari
		hagi donat el seu consentiment previ per a això d'acord amb el que s'indica a la finestra emergent del
		navegador de l'Usuari quan accedeix per primera vegada al Lloc Web i en els altres termes i condicions que
		s'indiquen
		en la Política de cookies de CUANTUM MEDICAL que tot Usuari ha de conèixer.</p>


	<h2>7. MENORS</h2>

	<p>Els menors d'edat no podran fer servir els serveis disponibles a través del Lloc Web sense la prèvia autorització
		dels seus pares, tutors o representants legals, els quals seran els únics responsables de tots els actes
		realitzats a través del Lloc Web pels menors a càrrec seu, incloent el compliment dels formularis
		amb les dades personals d'aquests menors i la marcació, si escau, de les caselles que els acompanyen.

	<h2>8. MESURES DE SEGURETAT</h2>

	<p>CUANTUM MEDICAL adopta els nivells de seguretat requerits pel RGPD adequats a la naturalesa de les dades
		que són objecte de tractament en cada moment.
	<p>No obstant això, la seguretat tècnica en un mitjà com internet no és inexpugnable i poden existir
		actuacions doloses de tercers, tot i que CUANTUM MEDICAL posa tots els mitjans al seu abast per evitar aquestes
		actuacions.

	<h2>9. ACTUALITZACIÓ I REVISIÓ</h2>

	<p>La present Política serà revisada i actualitzada sempre que sigui necessari per adaptar-la a les nostres
		pràctiques,
		serveis i a la normativa que sigui d'aplicació en cada moment, per la qual cosa li recomanem revisar aquesta
		Política cada vegada que accedeixi al nostre Lloc Web amb l'objectiu d'estar adequatament informat sobre com i
		per a què tractem les seves dades.
