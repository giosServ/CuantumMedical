	<h2>2. WHAT ARE COOKIES?</h2>
	<p>A cookie is a small text file that websites install on the computer or mobile device of users who visit them.
		Cookies perform many different tasks, for example, they allow us to know how many times the Website has been
		visited by the user, identify if the user has previously registered, remember their previous actions and
		preferences so that they do not have to configure them again when they return to the Website (login identifier,
		password, language, font size, and other display preferences), as well as allowing secure and stable navigation
		between pages.</p>
	<p>The cookies used on the Website do not, by themselves, reveal personal information. Their analytical purpose uses
		identifiers, including the IP address, solely for the purpose of identifying the number of unique visitors to
		the website, their geographical origin, or their web usage trend, but they do not personally identify the User.
	</p>

